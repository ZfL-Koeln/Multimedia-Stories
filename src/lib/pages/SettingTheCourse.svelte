	<article class="text-lg sm:text-xl md:text-2xl lg:text-xl 2xl:text-2xl 3xl:text-3xl h-screen p-16">
		<strong>WEICHENSTELLUNG für Viertklässler</strong> ist ein von der
		ZEIT-Stiftung Ebelin und Gerd Bucerius initiiertes
		Projekt. Es unterstützt Grundschulkinder beim
		Wechsel auf eine weiterführende Schule. Für viele
		Kinder ist dieser Wechsel die erste große Hürde, die
		sie in ihrem Leben bewältigen müssen. Nicht alle
		Kinder haben die Chance, von ihrem Umfeld
		unterstützt und nach ihren Begabungen gefördert zu
		werden. Hier setzt WEICHENSTELLUNG an. Das
		Projekt startete in Hamburg und Baden-Württemberg
		und wird seit 2016 auch in Köln vom Zentrum für
		Lehrer*innenbildung der Universität zu Köln
		umgesetzt. Das Konzept: Lehramtsstudierende
		begleiten die Kinder über einen Zeitraum von drei
		Jahren, jeweils vom Beginn der vierten Klasse bis
		zum Ende des sechsten Schuljahrs. Sie unterrichten
		sie, arbeiten mit ihnen in Lerngruppen und
		unternehmen einmal im Monat kulturelle
		Aktivitäten mit ihnen. Die studentischen
		MentorInnen entwickelten sich im Laufe der Zeit zu
		wichtigen Bezugspersonen für die Kinder. Sie selbst
		sammelten jede Menge Praxiserfahrung für ihren
		späteren Beruf.

