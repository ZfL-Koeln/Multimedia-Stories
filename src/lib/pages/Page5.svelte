        <header class="font-bold text-4xl md:text-6xl xl:text-8xl">UNGLEICHE BILDUNGSCHANCEN</header>
        <p class="w-2/5 leading-relaxed">
            Schulische Leistungen von Kindern und Jugendlichen in
            Deutschland entwickeln sich seit Jahren auseinander. Zu diesem
            Ergebnis kommen Studien der OECD und PISA, aber auch die
            Internationale Grundschul-Lese-Untersuchung IGLU. Grund dafür ist,
            dass außerschulische Faktoren, wie etwa das Elternhaus, starken
            Einfluss auf die Entwicklung von Kindern und Jugendlichen
            ausüben. Aber auch das deutsche Schulsystem trennt Schul- und
            Lebenswege früh und prägt Bildungskarrieren damit bereits im
            Grundschulalter. Kinder mit besonderem Lernbedarf sind hiervon
            besonders betroffen. Dabei können vor allem niedrigschwellige
            individuelle Förderangebote, gemeinsames Lernen und ein
            Beziehungsaufbau zwischen Lehrenden und Lernenden Kinder und
            Jugendliche stärken. Erfolgreich sind solche Angebote besonders
            dann, wenn sie in den Schulunterricht integriert sind. Positive
            Erfahrungen, die Kinder und Jugendliche in einem Projekt machen,
            der Erfolg beim Sport, der Besuch im Museum oder ein gemeinsames
            Erlebnis bei einer Stadtralley, können dann auf Schule übertragen
            werden. Es braucht daher ein Gesamtkonzept, das schulische und
            außerschulische Angebote nicht nur flächendeckend zur Verfügung
            stellt, sondern auch miteinander verzahnt.
        </p>
