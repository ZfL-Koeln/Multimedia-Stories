    <p class="font-bold flex-row">FÖRDERPROJEKTE HELFEN</p>
    <div>
        Das Zentrum für Lehrer*innenbildung der
        Universität zu Köln fördert bildungsbenachteiligte
        und neu zugewanderte Kinder und Jugendliche mit
        den Bildungsprojekten WEICHENSTELLUNG für
        Viertklässler und PROMPT! Bildungsteilhabe
        stärken. WEICHENSTELLUNG für Viertklässler ist
        ein Förderprogramm der ZEIT-Stiftung Ebelin und
        Gerd Bucerius. Es richtet sich an Grundschulkinder,
        die das Potenzial haben, eine weiterführende
        Schule zu besuchen, die aber für den Übergang
        Unterstützung benötigen. PROMPT!
        Bildungsteilhabe stärken wurde vom ZfL 2014 ins
        Leben gerufen und wendet sich an neu
        zugewanderte und bildungsbenachteiligte Kinder
        und Jugendliche.
    </div>
    <div>
        Sie sollen möglichst schnell am regulären
        Schulunterricht teilnehmen und einen
        Schulabschluss erreichen können. In beiden
        Förderlinien spielen Lehramtsstudierende eine
        Schlüsselrolle. Im Rahmen ihrer Praxisphasen
        lernen Studierende in eins-zu-eins-Situationen
        oder in Kleingruppen mit bildungsbenachteiligten
        Kindern und Jugendlichen – in der Schule oder in
        einer Notunterkunft. Der Austausch zwischen
        Studierenden und Schüler*innen stärkt den
        Beziehungsaufbau und leistet einen wichtigen
        Beitrag zum Lernerfolg der Kinder und
        Jugendlichen.

    </div>
